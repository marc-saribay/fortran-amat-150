PROGRAM TO COMPUTE THE SLOPE OF A LINE GIVEN 2 POINTS
INPUTS:X1,Y1
C     X2,Y2
OUTPUT:S=SLOPE

      PRINT*,'THIS IS MY SECOND PROGRAM'
      PRINT*,'ENTER FIRST POINT'
      READ*,X1,Y1
      PRINT*,'ENTER SECOND POINT'
      READ*,X2,Y2

TEST IF SLOPE IS DEFINED

      IF(X1.EQ.X2)THEN
            PRINT*,'SLOPE NOT DEFINED!'
      ELSE
            S=(Y1-Y2)/(X1-X2)
            PRINT*,'THE SLOPE IS',S
      ENDIF
      END